public class Code02_07_01 {

    public static void main(String[] args) {

        int num1, num2, result;
        num1 = 3000;
        num2 = 60;

        result = num1 + num2;
        System.out.println(num1 + "+" + num2 + "=" + result);

        result = num1 - num2;
        System.out.println(num1 + "-" + num2 + "=" + result);

        result = num1 * num2;
        System.out.println(num1 + "*" + num2 + "=" + result);

        result = num1/num2;
        System.out.println(num1 + "/" + num2 + "=" + result);

    }
}
